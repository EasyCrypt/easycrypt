  (* c2 = b::bl *)
  clear - Heq. apply False_ind.
  rewrite mapfst_nil in Heq. rewrite <- (surjective_pairing _ _ b) in Heq. rewrite mapfst_cons in Heq.
  exact (Nil_neq_Cons _ _ _ Heq).
  (* c2 = [] *)
  clear Heq.
  rewrite ischained_nil in *.
  rewrite <- Hch1. rewrite <- Hch2.
  tauto.

