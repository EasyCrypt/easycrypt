   (* case bl = [] *)
   rewrite ischained_nil in Hch1, Hch2. rewrite <- Hch2 in Hch1; clear Hch2.
   unfold Injective in HB2.
   exact (HB2 a b Hdoma Hdomb Hch1).
  (* case c2 = b::bl *)
  apply False_ind. clear - Heq.
  rewrite length_cons in Heq. rewrite length_nil in Heq.
