      clear - Hneq Heq e.
      rewrite Heq in Hneq; clear Heq.
      apply pair_inj in e. destruct e as (_,e).
      symmetry in e.
      exact (Hneq e).
      (* case (x,y) <> (x',y') *)
      rewrite (upd_map_dom_diff _ _ T (pair x y) (pair x' y') z n) in Hdom.
