    destruct Hin as [C|Hin].
     (* (x,y) = a *)
     clear - C n. symmetry in C.
     exact (n C).
     (* in_list (x,y) al *)
     clear - IH Hseq Hin.
     rewrite <- (Append_nil _ (Cons a al)) in Hseq.
     apply ischained_app in Hseq.
     destruct Hseq as (_,Hseq).
     eapply IH; eauto.

  (* case find_seq x' y' (upd_map T (pair x y) z) = None *)
  rewrite e, e0. reflexivity.
Qed.


